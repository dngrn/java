
class Arithmetic_operator
{
	public static void main(String[] args) {
		byte a=225;
		byte b=330;
		
		int sum=a+b;
		System.out.println(sum);
		
		int subtract=a-b;
		System.out.println(subtract);
		
		int multiply=a*b;
		System.out.println(multiply);
		
		int division=a/b;
		System.out.println(division);
		
		int modulo=a%b;
		System.out.println(modulo);
		
	}
}